-- Security patch: tighten client_error_events access
-- Run this once in Supabase SQL Editor on existing projects.

begin;

alter table public.client_error_events enable row level security;

drop policy if exists "client_error_events_insert_any" on public.client_error_events;
drop policy if exists "client_error_events_insert_own" on public.client_error_events;
create policy "client_error_events_insert_own" on public.client_error_events
for insert to authenticated
with check (auth.uid()::text = user_id);

drop policy if exists "client_error_events_select_own" on public.client_error_events;
create policy "client_error_events_select_own" on public.client_error_events
for select to authenticated
using (auth.uid()::text = user_id);

revoke insert, update, delete on table public.client_error_events from anon;
grant insert, select on table public.client_error_events to authenticated;

create table if not exists public.api_key_telemetry_events (
  id bigint generated by default as identity primary key,
  user_id text not null,
  event_type text not null default 'request_success',
  provider text not null default '',
  model text not null default '',
  key_fingerprint text not null default '',
  key_hint text not null default '',
  key_id text not null default '',
  task text not null default '',
  success boolean not null default true,
  status_code integer null,
  latency_ms integer not null default 0,
  prompt_chars integer not null default 0,
  response_chars integer not null default 0,
  estimated_tokens integer not null default 0,
  error_message text not null default '',
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now())
);

create index if not exists api_key_telemetry_events_user_id_created_at_idx
  on public.api_key_telemetry_events (user_id, created_at desc);

create index if not exists api_key_telemetry_events_provider_created_at_idx
  on public.api_key_telemetry_events (provider, created_at desc);

alter table public.api_key_telemetry_events enable row level security;

drop policy if exists "api_key_telemetry_events_insert_own" on public.api_key_telemetry_events;
create policy "api_key_telemetry_events_insert_own" on public.api_key_telemetry_events
for insert to authenticated
with check (auth.uid()::text = user_id);

drop policy if exists "api_key_telemetry_events_select_own" on public.api_key_telemetry_events;
create policy "api_key_telemetry_events_select_own" on public.api_key_telemetry_events
for select to authenticated
using (auth.uid()::text = user_id);

drop policy if exists "api_key_telemetry_events_select_admin" on public.api_key_telemetry_events;
create policy "api_key_telemetry_events_select_admin" on public.api_key_telemetry_events
for select to authenticated
using ((auth.jwt() ->> 'email') = 'ductruong.lynx@gmail.com');

revoke insert, update, delete on table public.api_key_telemetry_events from anon;
grant insert, select on table public.api_key_telemetry_events to authenticated;

commit;
